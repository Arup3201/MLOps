
# Loops 

# For loop

# Syntax:

# for variable in sequence:

#     statements

# Example:

# Print numbers from 1 to 5

for i in range(1, 6):
    
        print(i)

# While loop

# Syntax:

# while condition:

#     statements

# Example:

# Print numbers from 1 to 5

i = 1

while i < 6:
        
            print(i)
            
            i += 1

# Break Statement

# Syntax:

# break

# Example:

# Exit the loop when i is 3

for i in range(1, 6):
        
            if i == 3:
                
                break
                
            print(i)

# Continue Statement

# Syntax:

# continue

# Example:

# Skip the iteration when i is 3

for i in range(1, 6):
            
                if i == 3:
                    
                    continue
                    
                print(i)

                