
# Data Types

# 1. Numbers

# Integers

x = 1

print(x)

# Floats

y = 1.0

print(y)

# Complex Numbers

z = 1 + 2j

print(z)

# 2. Strings

name = "John"

print(name)

# 3. Lists

fruits = ["apple", "banana", "cherry"]

print(fruits)

# 4. Tuples


fruits = ("apple", "banana", "cherry")

print(fruits)

# 5. Sets

fruits = {"apple", "banana", "cherry"}

print(fruits)

# 6. Dictionaries

person = {"name": "John", "age": 36, "country": "Norway"}

print(person)

# 7. Booleans

x = True

print(x)

# 8. None

x = None

print(x)


# Type Conversion

# 1. Implicit Type Conversion

x = 1

y = 1.0

z = x + y

print(z)

# 2. Explicit Type Conversion

x = 1

y = 1.0

z = x + int(y)

print(z)

# 3. Type Conversion Functions

# int()

x = 1.0

y = int(x)

print(y)

# float()

x = 1

y = float(x)

print(y)

# str()

x = 1

y = str(x)

print(y)

# list()

x = (1, 2, 3)

y = list(x)

print(y)

# tuple()

x = [1, 2, 3]

y = tuple(x)

print(y)

# set()

x = [1, 2, 3]

y = set(x)

print(y)

# dict()

x = [("name", "John"), ("age", 36)]

y = dict(x)

print(y)

# bool()

x = 1

y = bool(x)

print(y)

# None

x = None

print(x)

